numero= int(input("Digite um número: "))

if numero == 1:
    print("Domingo")
elif numero == 2:
    print("Segunda-Feira")
elif numero == 3:
    print("Terça-Feira")
elif numero == 4:
    print("Quarta-Feira")
elif numero == 5:
    print("Quinta-Feira")
elif numero == 6:
    print("Sexta-Feira")
elif numero == 7:
    print("Domingo")
else:
    print("Número Inválido")